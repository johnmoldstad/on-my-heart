<p>Here are some ways to engage kids with the program and help them excel.</p>
<p>On My Heart makes a great Sunday school opener. Tips for Sunday school teachers:</p>
<ul>
	<li>Write out the week's material on an easel pad or use a projector so that the older students can read along. </li>
	<li>When introducing the week's material, read each section once, then have everyone say it with you. This way the students see, hear, and say the new material. </li>
	<li>The books of the Bible can be reviewed weekly by having the students say all the books from Genesis (or Matthew) up through the new books for the week.</li>
	<li>Use motions to help the students remember the timeline events. The timeline can also be reviewed weekly, with motions.Â  </li>
	<li>Certificates and prizes can be great motivators.</li>
</ul>
<p>Tips for parents:</p>
<ul>
	<li>Encourage your child to set a goal at the beginning of the semester. Start with one or two categories at first (maybe the books of the Bible and the timeline), and add more in later semesters. </li>
	<li>Make On My Heart a part of your daily devotion time. After dinner or before bed are good times to make it part of the routine. Another good time to review memory work is during car rides.</li>
	<li>Memorize the material along with your child. Memory work is good for all of us!</li>
	<li>Encourage your child to recite their memory work to their teacher early and often in the semester. Reciting every week is best. It can be hard to catch up once you fall behind.</li>
</ul>
